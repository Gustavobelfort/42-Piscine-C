#include <unistd.h>

void ft_putstr(char *s)
{
	int i;
	
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
}

int main()
{
	ft_putstr("9876543210");
	write(1, "\n", 1);
}
